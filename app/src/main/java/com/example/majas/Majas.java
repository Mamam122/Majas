package com.example.majas;

class Majas {
    private String majas, gambar, pengertian,contoh;

    public String getMajas() {
        return majas;
    }

    public void setMajas(String majas) {
        this.majas = majas;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getPengertian() {
        return pengertian;
    }

    public void setPengertian(String pengertian) {
        this.pengertian = pengertian;
    }

    public String getContoh() {
        return contoh;
    }

    public void setContoh(String contoh) {
        this.contoh = contoh;
    }
}
